    <h3>Utensils:</h3>
    <ul>
	<li>**  Pan - 2   **</li>
	<li>**  Plate to serve  **</li>
    <li>**  Wooden spatula  **</li></ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**Chicken Drumsticks/chicken wings -6**</li>
        <li>**Ginger-garlic paste-1 table spoon**</li>
        <li>**All-purpose flour (maida)- 2 table spoon**</li>
        <li>**Corn flour or gram flour- 2 table spoon**</li>
        <li>**Eggs (beaten) - 2**</li>
        <li>**Pepper / chili powder-1/4 table spoon**</li>
        <li>**A pinch of sugar**</li>
        <li>**Salt To Taste**</li>
        <li>**Oil for frying**</li>
        <li>**Soya sauce1 table spoon**</li>
    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**In a bowl add Soya sauce, ginger-garlic paste, sugar.**</li>
        <li>**Add chicken wings coat well and set aside for at least 1 hour. (you can put it even more for a whole night to get better taste)**</li>
        <li>**Make a smooth batter by adding corn flour, all-purpose flour, salt, pepper and beat in the eggs.**</li>
        <li>**Heat oil in a pan (you can heat the oven for 350 f and let the chicken drumsticks cook for 35 minutes)**</li>
        <li>**Dip each wing in the batter and coat evenly and deep fry the drumsticks till golden brown.**</li>
    </ol>

    <h2>Your Chicken Drumstick is ready to serve.</h2>
