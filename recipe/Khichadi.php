    <h3>Utensils:</h3>
    <ul>
	<li>**  Presser cooker   **</li>
	<li>**  Spoon   **</li>
    <li>**  Bowl **</li></ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Maas dal -1 cup  **</li>
        <li>**  Rice – ½ cup  **</li>
        <li>**  Ginger- 1 table spoon   **</li>
        <li>**  Turmeric- ½ tea spoon **</li>
        <li>**  Ghee- 2 table spoon **</li>
        <li>**  Salt- to taste  **</li>
        <li>**  Asfatedo (hing) 1 pinch   **</li>
        <li>**  Water- 2 cups  **</li>
       
    </ul>
	

<h3>Method:</h3>
    <ol>
      
        <li>**  Wash and soak dal and rice for about half an hour in a bowl  **</li>
        <li>**  Take a presser cooker and add soaked rice and dal  **</li>
        <li>**  Now add salt, turmeric, ginger garlic and hing   **</li>
        <li>**  Also add ghee   **</li>
        <li>**  Add water and cover the cooker  **</li>
        <li>**  Switch on the gas and let it give 5 whistles   **</li>
        <li>**  Switch off the gas and let it stay there for other 5 minutes  **</li>

    <h2>Khichadi is ready. Serve it in a plate and serve it hot !</h2>
