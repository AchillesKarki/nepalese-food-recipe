    <h3>Utensils:</h3>
    <ul>
	<li>**  a big pot **</li>
    <li>**  spatula  **</li></ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**Water- 1 liter**</li>
        <li>**Flour (any type best is corn flour)-200gram**</li>
    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**Heat a heavy pan on the gas (kasaudi is best)**</li>
        <li>**Put the water in the pan adding a pinch of flour in it and cover it to let it boil.**</li>
        <li>**Let the water boil properly, if the water is not well boiled dhido may not be well cooked.**</li>
        <li>**Add 2 spatula of flour in the water and stir it properly.**</li>
        <li>**You have to continuously stir the dhido till it gets cooked.  (otherwise  it  will have balls on dhido) **</li>
        <li>**Let it cook for about 5 minutes**</li>
    </ol>

    <h2>Your dhido is ready to serve.</h2>
