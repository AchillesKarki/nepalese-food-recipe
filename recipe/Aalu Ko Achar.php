    <h3>Utensils:</h3>
    <ul>
	<li>** Pan  **</li>
	<li>** Pressure Cooker  **</li>
         </ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**   Potato — 2 lbs  **</li>
        <li>**   Cooking oil –half a cup **</li>
        <li>**   Sesame seeds– half a cup **</li>
        <li>**   Cumin powder –1 spoon **</li>
        <li>**   Fenugreek seeds (Methi) - Some **</li>
        <li>**   Turmeric powder — 1/5 spoon **</li>
        <li>**   Coriander powder — 1 spoon **</li>
        <li>**   Asafoetida powder (heeng) (optional)– pinch **</li>
        <li>**   Green chilies – 5 – 10Lemon juice — half cup **</li>
        <li>**   Coriander leaves– few **</li>
        <li>**   Salt- your taste **</li>
      
    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**  Boil potatoes, peel it and chop into quarters  **</li>
        <li>**  Roast sesame seeds (Teel) (half cup) on a pan till it turns light brown and starts crackling, after which grind it in a food processor  **</li>
        <li>**  Heat half cup of oil in frying pan and add about 30 – 40 methi seeds till it turns dark brown  **</li>
        <li>**  Add cut green chilies (about 6 – 9 depending on your taste) light fry chilies for 20 seconds   **</li>
        <li>**  Add spoonful of coriander, cumin and 1/5 of turmeric powder   **</li>
        <li>**  Add the ground sesame seeds and salt to taste (You may add pinch of Asafoetida)  **</li>
        <li>**  Cook for a minute in low flame. Stir it continuously **</li>
        <li>**  Add the potatoes cook for about 3-4 minutes (stirring continuously)**</li>
        <li>**  Add half cup of fresh lime/lemon juice. Mix it very well  **</li>
        <li>**  Garnish with fresh chopped coriander leaves  **</li>

    </ol>

    <h2>Your Aalu Ko Achar is ready to serve.</h2>
