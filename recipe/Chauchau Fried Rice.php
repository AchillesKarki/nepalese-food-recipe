    <h3>Utensils:</h3>
    <ul>
	<li>**  patula **</li>
	<li>**  Pan  **</li>
   </ul>
 
    <h3>Ingredients:</h3>
	<ul>

        <li>** Mustard oil- 1 table spoon (you can use ghee, butter or any other type of oil, but fried rice tastes best in mustard oil)    **</li>
        <li>** Salt- as per taste     **</li>
        <li>** Red chili powder- ½ tea spoon     **</li>
        <li>** Turmeric – a pinch     **</li>
        <li>** Cooked rice (pakeko bhat)    **</li>
        <li>** Cumin seed- 1 tea spoon     **</li>
        <li>** Wai wai noodles- 1 packet  **</li>

    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>** Turn on the gas and het he pan  **</li>
        <li>** Add oil, when oil is hot add cumin seed  **</li> 
        <li>** Now add the rice and stir it  **</li>
        <li>** Add salt, turmeric and red chili powder  **</li> 
        <li>** Stir and mix it properly   **</li>
        <li>** Cook without covering for about 2 minute   **</li>
        <li>** Now add waiwai   **</li>
        <li>** Stir it properly to mix   **</li>
        <li>** Let it be for about 3 minutes  **</li> 
        <li>** Turn off the gas   **</li>


    </ol>

    <h2>Noodle fried rice (चाउचाउ र भात भुटेको) is ready to serve! Enjoy!  </h2>
