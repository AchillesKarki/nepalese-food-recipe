    <h3>Utensils:</h3>
    <ul>
	<li>**  Pan to boil milk  **</li>
	<li>**  Pan to make haluwa **</li>
    <li>**  Spatula **</li>
</ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Carrots Grated-2 cups    **</li> 
        <li>**  Milk-2 cups    **</li>
        <li>**  Sugar-1/4 cup    **</li>
        <li>**  Cardamom powder-1/2 teaspoon    **</li>
        <li>**  Chopped cashew nuts -6    **</li>
        <li>**  Ghee – ½ table spoon    **</li>

    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**  Switch on the stove and boil the milk   **</li>
        <li>**  In another pan melt the ghee   **</li>
        <li>**  Add grated carrots and fry for about seven minutes  **</li>
        <li>**  When carrot turns slightly golden brown add milk   **</li>
        <li>**  Cook this mixture till all milk dries  **</li>
        <li>**  Add sugar and cardamom powder and mix well  **</li>
        <li>**  Cook for four more minutes   **</li>
        <li>**  Garnish with cashew nuts  **</li>
        <li>**  Turn off the stove gajar haluwa is ready to serve  **</li> 
        <li>**  You can refrigerate this halwa in a greased plate  **</li>
        <li>**  Cut it on the shape of barfi  gajar barfi is ready   **</li>

    </ol>

    <h2>Your Gajar Ko Haluwa is ready to serve.</h2>
