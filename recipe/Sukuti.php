    <h3>Utensils:</h3>
    <ul>
	<li>**  Big bowl  **</li>
	<li>**  Knife  **</li>
    <li>**  Dry Cloth or metal wire  **</li></ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Meat loaf – 2 lbs **</li>
        <li>**  Cumin powder **</li>
        <li>**  Coriander powder  **</li>
        <li>**  Chili powder **</li>
        <li>**  Turmeric powder **</li>
        <li>**  Salt, pepper  **</li>
        <li>**  Oil – Very less **</li>
       
    </ul>
	

<h3>Method:</h3>
    <ol>
        
        <li>**  Cut the meat carefully into long thin strips   **</li>
        <li>**  Remove all the fat one by one  **</li>
        <li>**  Put the strips into a big bowl **</li>
        <li>**  Marinate it with salt, cumin, coriander, pepper, chili powder and turmeric, ginger and garlic pastes **</li>
        <li>**  Keep the marinated meat in Fridge for 24 hours to suck in all the ingredients **</li>
        <li>**  Spread it out on a clean cloth and put in under the sun or hang on metal wire above the cooking stove in the kitchen (it is better to put in sun for a while before you keep it in kitchen)   **</li>
        <li>**  Once the meat becomes dry store it and eat it as you want.  **</li>
       
    </ol>

