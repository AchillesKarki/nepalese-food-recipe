    <h3>Utensils:</h3>
    <ul>
	<li>** Big  **</li>
	<li>** Big Bowl  **</li>
        <li>** Knife  **</li>
        <li>** Stirrer  **</li></ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Bhagate or pomelo / grapefruit-1   **</li>
        <li>**  Oranges – 3 or as you desire  **</li>
        <li>**  Plain yogurt- ½ cup  **</li>
        <li>**  Sesame powder 2 table spoon   **</li>
        <li>**  Salt -to taste   **</li>
        <li>**  Sugar- 1 table spoon   **</li>
        <li>**  Red chili pepper- 1 table spoon   **</li>
        <li>**  Cooking oil- 1 table spoon **</li>
        <li>**  Fenugreek seeds (methi) -¼ teaspoon **</li>
        <li>**  Turmeric -a pinch  **</li>
    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**  Peal Bhagate fruits and put it in a bowl   **</li>
        <li>**  Cut oranges into two halves, put it in the same bowl with Bhagate  **</li>
        <li>**  Add yogurt, salt, sugar, sesame powder, red chili powder in the bowl and stir it well   **</li>
        <li>**  Heat a pan, pour cooking oil onto it   **</li>
        <li>**  When the oil gets hot put fenugreek seed on it  **</li>
        <li>**  Let fenugreek seed turn golden brown, take off the oil with fenugreek (methi) from the stove and pour it into the bhagate mixture in the bowl   **</li>
        <li>**  Stir it and mix it well (you can use your clean hand to mix it too)  **</li>
        

    </ol>

    <h2>Sadeko Bhogate is ready to serve, Enjoy!</h2>
