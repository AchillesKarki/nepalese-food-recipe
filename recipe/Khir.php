    <h3>Ingredients:</h3>
	<ul>
       <li>**   Rice – 1 cup    **</li>
       <li>**   Milk – 2 cups    **</li>
       <li>**   Ghee – 1 table spoon    **</li>
       <li>**   Sugar – Half cup    **</li>
       <li>**   Cardamom – 1 Tablespoon    **</li>
       <li>**   Cashew-nuts – some    **</li>
       <li>**   Raisins – some    **</li>
       <li>**   Big Cardamom – Alaichi (Eliechy)- a little     **</li>
       <li>**   Black Pepper – pinch    **</li>

    </ul>
	

<h3>Method:</h3>
    <ol>
      <li>**    Boil milk with sugar.  **</li>
      <li>**    Mix rice with ghee (do not wash rice)  **</li>
      <li>**    Pour rice in the hot boiled milk and stir it continually.  **</li>
      <li>**    Boil and cook the mixture for few more minutes till rice gets cooked.  **</li>
      <li>**    Add cardamom, cashew, raisins, black pepper when the khir is about to get ready. (When it b.ecomes thick)  **</li>
      <li>**    Khir is ready serve hot or even you can keep it and have it cold. Khir is delicious hot or cold   **</li>


    </ol>

