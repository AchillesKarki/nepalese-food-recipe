              <p>Jeri, also known as zulbia, is a sweet popular in countries of South Asia, West Asia, North Africa, and East Africa. It is made by deep-frying maida flour (plain flour or all-purpose flour) batter in pretzel or circular shapes, which are then soaked in sugar syrup.</p>
