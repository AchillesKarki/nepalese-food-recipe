    <h3>Ingredients:</h3>
	<ul>
    <li>**   All-purpose flour (maida)- 2 cups  **</li>
    <li>**   Fine grained semolina or rice flour-1 and 1/2 table spoon  **</li>
    <li>**   Baking powder-1/4th table spoon  **</li>
    <li>**   Curd (plain yogurt) -2 table spoon  **</li>
    <li>**   Warm water-11/4th cups  **</li>
    <li>**   Saffron threads, slowly dry-roasted and powdered-1/2 table spoon  **</li>
    <li>**   Sugar-3 cups  **</li>
    <li>**   Water-2 and 2/3rd cups  **</li>
    <li>**   Green cardamom seeds powder-1/2 table spoon  **</li>
    <li>**   Rose water-1 and 1/2 table spoon  **</li>
    <li>**   Ghee or vegetable oil for frying  **</li>
    </ul>
	

<h3>Method:</h3>
    <ol>
       <li>**   Mix the flour, semolina or rice flour, baking powder, curd and 3/4th cup of the water in a bowl (preferably a ceramic bowl).  **</li>
       <li>**   Mix well with a whisk.  **</li>
       <li>**   Mix well and then add remaining water and 1/8th tsp. of saffron powder, and whisk until smooth.  **</li>
       <li>**   Set aside for about 2 hours to ferment.  **</li>
       <li>**   Whisk thoroughly before use.  **</li>
       <li>**   Prepare one string syrup by dissolving sugar in the water.  **</li>
       <li>**   Just before the syrup is ready add saffron and cardamom powder.  **</li>
       <li>**   Heat oil in a pan.  **</li>
       <li>**   Pour the batter in a steady stream (or coconut shell with a hole) into the pan to form coils.  **</li>
       <li>**   Make a few at a time.  **</li>
       <li>**   Deep fry them until they are golden and crisp all over but not brown.  **</li>
       <li>**   Remove from the pan and drain on kitchen paper and immerse in the syrup.  **</li>
       <li>**   Leave for at least 4-5 minutes so that they soak the syrup.  **</li>
       <li>**   Take the jeri out of syrup and serve hot.  **</li>
  **</li>

    </ol>

