    <h3>Utensils:</h3>
    <ul>
	<li>**  Steamer **</li>
	<li>**  Big bowl **</li>
    <li>**  Soft piece of cloth **</li>
    <li>**  Pan **</li>
    <li>**  Spatula **</li>
</ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Rice flour- 500 grams     **</li>
        <li>**  Chakku (Molasses)-100 grams     **</li>
        <li>**  Sesame seed Powder – 50 gram     **</li>
        <li>**  Coconut powder/ grated coconut-20 grams     **</li>
        <li>**  Cardamom powder- 5 grams     **</li>
        <li>**  Vegetable oil- 10 gram     **</li>

    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**  Heat a cup of water and chaku in pan at a medium heat    **</li>
       <li>**  When chaku melts, add sesame powder, coconut powder and cardamom powder and stir it gently.    **</li>
       <li>**  Add about a spoon full of rice flour and stir it gently    **</li>
       <li>**  When the paste is good (not too thick or too thin) pour it into a bowl    **</li>
       <li>**  Make the dough for yomari using rice flour and Luke warm water    **</li>
       <li>**  Make a soft dough (dough should be soft because if the dough is hard the yomari will break)    **</li>
       <li>**  When the dough is ready cover it with soft piece of cloth and leave it for about half an hour.    **</li>

        <li>                 After half an hour,   </li>
        <li>**  Grease your palm with cooking oil so that the dough will not stick to your hand   **</li>
        <li>**  Take a small piece of dough and make it first into round then into long yomari shape   **</li>
        <li>**  Make as thin as you can because more thin your wrap is more tasty yomari becomes   **</li>
        <li>**  Add two teaspoon of chhaku into the wrap and slowly close the yomori top.   **</li>
        <li>**  You should keep on roiling the yomari while closing the top.   **</li>
        <li>**  Heat the water in the steamer.   **</li>
        <li>**  When water boils, put yomari in steamer and let it steam for about 10 minutes.   **</li>
        <li>**    **</li>
        <li>**    **</li>
        <li>**    **</li>

    </ol>

