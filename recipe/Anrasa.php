    <h3>Utensils:</h3>
    <ul>
	<li>**  Big pan for frying - 1  **</li>
	<li>**  Spatula - 1 **</li>
        <li>**  Plate - 2 **</li>
	<li>**  Big bowl - 1 **</li>
        <li>**  Grinder - 1 **</li>
    </ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**   Rice soaked for a few hours - 3 cups   **</li>
        <li>**   Ghee (Melted unsalted butter) -1 cup  **</li>
        <li>**   Sugar - 3 cups  **</li>
        <li>**   Cardamom (ground) - 1 tablespoon **</li>
        <li>**   Almond nuts (ground) - 1/4 cup **</li>
        <li>**   Cashew nuts (ground) - 1/4 cup **</li>
        <li>**   Ghee (Melted butter for frying) - 2 cups **</li>
        <li>**   Lemons: 4 to 5 **</li>
        <li>**   White sesame seed- half cup **</li>
        
    </ul>
	

<h3>Method:</h3>
    <ol>
   <li>**  Day I  **</li>


        <li>**  Grind soaked rice and sugar  **</li>
        <li>**  Add ground cardamom, cashew and almond and mix it well **</li>
        <li>**  Put the mixture as it is to stay overnight, the mixture gets moist next morning  **</li>
       
        
   <li>** Day 2   **</li>


        <li>**  Draw lemon juice in a bowl  **</li>
        <li>**  Take a plate and spread some sesame seed and some dried ground rice on it  **</li>
        <li>**  Take a small ball out of the moist mixture   **</li>
        <li>**  Dip you figures into the lemon juice, gently spread the ball (in the plate of sesame seed and rice) into the shape of a cookie with your lemon wet figures  **</li>
        <li>**  Heat oil in a big pan until it starts steaming (make it real hot)  **</li>
        <li>**  Drop Anarsha into the oil  **</li>
        <li>**  Change sides and let it fry one at a time until it becomes golden brown **</li>
        <li>**  Take the Anarsha out when it is golden brown and stack it one on top of other (Place on a sheet of paper towel to absorb excess oil)   **</li>
        <li>**  Place it to be cool.   **</li>


    </ol>

