              <p>Anarsa is a type of rice cookie. It is generally made during festivals. It is offered as Prasad to God too. Anarshas are made mainly during Deepawali. Anarsha made of mixture in about equal proportion of rice flour and sugar, hence it is sweet enough to call Mithai. The sesame seeds on the top adds different flavor to it. The ingredient mixture of rice flour, sugar and ground dry fruits are kept overnight to mix properly. Making of Anarsa is two days process, if you want to make it all at home.</p>
