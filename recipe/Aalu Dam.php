    <h3>Utensils:</h3>
    <ul>
	<li>** Big Pan  **</li>
	<li>** Preesure cooker  **</li>
    </ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Small potatoes-2 lbs   **</li>
        <li>**  Onion (chopped)-1 cup  **</li>
        <li>**  Garlic paste-1 teaspoon **</li>
        <li>**  Ginger paste- 1 teaspoon **</li>
        <li>**  Tomato (chopped) - ½ cup  **</li>
        <li>**  Red chili powder-1 teaspoon  **</li>
        <li>**  Methi (fenugreek seed)- Few **</li>
        <li>**  Coriander powder-1 teaspoon **</li>
        <li>**  Turmeric powder-1 teaspoon **</li>
        <li>**  Garam masala-1 teaspoon  **</li>
        <li>**  Cashew nut pieces or paste (optional) -3 teaspoon   **</li>
        <li>**  Yogurt (curd) -½ cup   **</li>
        <li>**  Oil for deep fry   **</li>
        <li>**  Salt to taste  **</li>
    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**  Boil potatoes until half cooked. Let it cool   **</li>
        <li>**  Peel it. Prick potatoes with a fork. Deep fry until potatoes are golden brown.  **</li>
        <li>**  Heat 2 teaspoon oil in separate utensil, add methi (fenugreek seed) Add onion when methi turns dark. Cook onions until light brown.  **</li>
        <li>**  Add cashew nut and yogurt. Add ginger paste, garlic paste, red chili powder, garam masala, coriander powder, and turmeric powder, Stir for a few seconds.  **</li>
        <li>**  Add tomato and cook on a medium heat for few minutes. Add salt  **</li>
        <li>**  Add 1.5 cups of water. Bring sauce to boil. **</li>
        <li>**  Add fried potatoes. Cook for 8 to 10 minutes on low heat.  **</li>
   

    </ol>

