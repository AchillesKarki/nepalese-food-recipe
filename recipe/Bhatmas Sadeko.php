    <h3>Utensils:</h3>
    <ul>
	<li>** Pan  **</li>
	<li>** Bowl  **</li>
        <li>** Spoon  **</li>
        <li>** Motor/crusher(lohoro)  **</li>
        </ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**   Dried Soya beans (bhatmas)- ½ lb (full or broken into halves)  **</li>
        <li>**   Green chili pepper – 1 finely cut into pieces  **</li>
        <li>**   Onion -1 finely chopped  **</li>
        <li>**   Ginger – about 1 tablespoon finely chopped  **</li>
        <li>**   Garlic – about 1 tablespoon finely chopped  **</li>
        <li>**   Green topped onion- 2 finely chopped  **</li>
        <li>**   Coriander leaves- 1 table spoon finely chopped **</li>
        <li>**   Lemon juice-1 teaspoon  **</li>
        <li>**   Red chili powder-1 teaspoon  **</li>
        <li>**   Salt as per taste  **</li>
        <li>**   Oil – 1 teaspoon  **</li>
        <li>**   Thyme seed -1/2 teaspoon **</li>
    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**   Fry soybeans (with oil or without oil) in a pan   **</li>
        <li>**   Spread fried soybeans in a big plate and roll the motor over it (to break soybeans into two halves and peels soybean covers out)   **</li>
        <li>**   Filter the broken soybean covers out from the Plate  **</li>
        <li>**   Pour the broken soybeans in the bowl and add all finely chopped ginger, garlic, onion, green topped onionAdd salt, red chili powder and lemon juice and coriander leaves to it  **</li>
        <li>**   Stir and mix it well and add green chili pepper on it  **</li>
        <li>**   Heat a pan, pour oil, heat oil and add thyme seed into hot oil  **</li>
        <li>**   When thyme turns golden brown put the heat off and add turmeric powder   **</li>
        <li>**   Pour this oil and thyme mixture to the soybean mixture you made before  **</li>

    </ol>

    <h2>Your bhatmas sadeko is ready to serve.</h2> 
    <h2>It tastes best with beaten rice.</h2>
