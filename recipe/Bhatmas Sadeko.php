              <p>Bhatmas Sadeko (Soybean Salsa) is a traditional Nepali dish, mainly popular in Newari community. It is a must have dish for numerous festivals, Newari community has. It is served as an appetizer course with flatten rice, fish, ginger and garlic. Altogether this dish is called "Samay Baji.</p>
