    <h3>Utensils:</h3>
    <ul>
	<li>**  Pan  **</li>
	<li>**  Bowl **</li>
    <li>**  Spoon **</li></ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Thyme seed- 1 table spoon  **</li>
        <li>**  Cumin powder- a pinch  **</li>
        <li>**  Ginger (finely crushed) - ½ table spoon  **</li>
        <li>**  Asfatea (hing) – a pinch **</li>
        <li>**  Turmeric -1/2 tea spoon **</li>
        <li>**  Salt as per taste **</li>
        <li>**  Ghee- ½ table spoon  **</li>
        <li>** Water- 2 cups (usually we soak rice and use the water (chaulani)) **</li>
        <li>**  Rice flour – ½ table spoon  **</li>
    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**  Turn on the stove and heat the pan **</li>
        <li>**  When the pan is hot add ghee  **</li>
        <li>**  As soon as ghee melts add hing, ginger and thyme seed and stir it   **</li>
        <li>**  Now add turmeric, cumin powder salt and rice flour, keep on stirring  **</li>
        <li>**  After 2 minutes add water  **</li>
        <li>**  Let it boil for about 5 minutes  **</li>
        <li>**  Jwana ko jhol is ready to serve  **</li>
        <li>**  You have to serve hot jwana ko jhol   **</li>
       
    </ol>

