    <h3>Utensils:</h3>
    <ul>
	<li>** Pan  **</li>
	<li>** Plate **</li>
        <li>** Spatula **</li></ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Gram flour / Garbanzo Flour- 500 gram (5 cups)  **</li>
        <li>**  Ghee - 400 Gram (2 cups)  **</li>
        <li>**  Sugar – 500 gram (2 cups)  **</li>
        <li>**  Cashew - 50 gram (quarter cup)   **</li>
        <li>**  Cardamom- 4 to 5 (powdered)  **</li>

        <li> For Sugar Mixture</li>


        <li> The sugar mixture should be made before you start making ladoos  </li>
        <li>**  Sugar-2 cups **</li>
        <li>**  Water **</li>
        <li>**  Oil for frying **</li>
       
    </ul>
	

<h3>Method:</h3>
    <ol>
        <li>**  Heat the pan on stove pour ghee on it  **</li>
        <li>**  Heat the pan till ghee gets hot (see ghee smoke and good smell is coming out of it)  **</li>
        <li>**  Pour gram flour into the hot pan   **</li>
        <li>**  Keep stirring until the flour turns golden brown  **</li>
        <li>**  Take pan out of heat  **</li>
        <li>**  Add cashew and cardamom powder and stir it well   **</li>
        <li>**  Let the flour cool down for a while and add sugar or sugar syrup on it (don’t add sugar on hot flour, sugar will melt)   **</li>
        <li>**  Take out small portion of cooked flour dough and make balls by gently rolling each portion between your palms(-if needed apply ghee on your palms so that the dough won’t stick to your hands) **</li>
        <li>**  Place the balls on a plate   **</li>

    </ol>

    <h2>Your Besan Ko Ladoo is ready to serve.</h2>
