    <h3>Utensils:</h3>
    <ul>
	<li>**  Pan - 2   **</li>
	<li>**  Spatula 1  **</li>
    <li>**  Bowl for serving  **</li></ul>
 
    <h3>Ingredients:</h3>
	<ul>
        <li>**  Rice noodles (Spaghetti) -1 lb  **</li>
        <li>**  Chicken Broth- 6 cups  **</li>
        <li>**  Grilled chicken breast, cut into very thin 1/8 inch slices -1/2 lb  **</li>
        <li>**  Carrot- cut in cube 1/2 cup  **</li>
        <li>**  Red bell pepper cut in cubes -1/2 cup  **</li>
        <li>**  Cooking oil -1 tablespoon  **</li>
        <li>**  Salt and pepper, as needed  **</li>
        <li>**  Chopped cilantro (to garnish) -1 tablespoon  **</li>
    </ul>
    
    <h3>Ingredients For Soup Paste:</h3>
        <li>**  Ginger, minced 1 teaspoon  **</li>
        <li>**  Red Chili Pepper-2 fresh  **</li>
        <li>**  Tomatoes -1/2 cup  **</li>
        <li>**  Fresh cilantro minced -1 tablespoon  **</li>
        <li>**  Cumin seed-1 teaspoon toasted  **</li>
        <li>**  Turmeric -1/2 teaspoon  **</li>
        <li>**  Peppercorn Szechwan (timur)-1/2 teaspoon  **</li>
        <li>**  Asafoetida (Hing) powder- 1/16 teaspoon  **</li>
        <li>**  Lime Juice- 1 table spoon freshly squeezed  **</li>
	

<h3>Method:</h3>
    <ol>
        <li>**  Boil half liter water in a big pan.  When the water starts having bubbles add the Spaghetti.  **</li>
        <li>**  In the same time take a blender and mix all soup paste ingredients into a thick paste; reserve in a small   bowl.  **</li>
        <li>**  When the spaghetti is soft take it off the heat and soak the excess water.  **</li>
        <li>**  Wash the spaghetti in running cold water.  **</li>
        <li>**  In a sauce pan heat cooking oil; add soup paste, fry for about 30 seconds  **</li>
        <li>**  Add chicken broth and mix well.  **</li>
        <li>**  Bring to a boil; set heat to low and cook for a few minutes; adjust seasoning with salt and pepper.  **</li>
        <li>**  Add all vegetables into the soup mixture; cook for a few minutes or until tender.  **</li>
        <li>**  Add the Spaghetti in the mixture  **</li>
        <li>**  You may add more broth if a soupier consistency is desired.  **</li>
        <li>**  Adjust seasoning with salt and pepper.  **</li>
        <li>**  To serve, pour soup broth into a serving bowl and add grilled chicken slices and cooked rice noodles.  **</li>
    </ol>

    <h2>Serve immediately.
Add hot chili sauce or tomato achar to the soup if you desire spicy soup broth.
</h2>
